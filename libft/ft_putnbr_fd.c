#include <unistd.h>

void	recc_w(int n, int fd)
{
	char	c;

	if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n < 10)
	{
		c = n + '0';
		write (fd, &c, 1);
	}
	else
	{
		recc_w(n / 10, fd);
		c = n % 10 + '0';
		write (fd, &c, 1);
	}
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	else
		recc_w (n, fd);
}
